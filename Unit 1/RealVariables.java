public class RealVariables {
    public static void main( String args[]){
    
        // Real number - any number with a remainder
        // Could be at any point on a numer line
        
        // float - 32 bits
        // This datatype can only store up to 6 significant decimal digits 
        
        float num1 = 1.01234567f;
        
        System.out.println(num1);
        
        // Double - 64 bits
        // can store up to 15 significant decimal digits
        
        double num2 = 1.01234567890123456789;
        System.out.println(num2);
        
        // Literal values
        
        int num3 = 10; // 10 is an int literal
        double num4 = 1.5; // 1.5 is a double literal
        
        System.out.println("Hello"); // "Hello" is a String literal
        
    }
}