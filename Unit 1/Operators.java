public class Operators{
    public static void main (String args[]){
    
    int num1 = 25;
    int num2 = 10;
    int result = 0;
    
    result = num1 + num2;
    
    System.out.println(num1 + " + " + num2 + " = " + result);
    
    // Integer division
    
    result = num2 / num1;
    
    // System.out.println(100 / result);
    // will get error java.lang.ArithmeticException: / by zero
    
    
    // Modulus Division
    
    result = num1 % num2;
    
    System.out.println(result);
    
    
    
    
    
    
    
    
    
    
    
    }

}