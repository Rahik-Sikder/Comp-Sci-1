public class WhilePractice {
    public static void main (String args[]){
    
        int loop1 = 1;
        while(loop1 <= 25){
            System.out.print(loop1 + " ");
            loop1++;
        }
        
        System.out.println("\n");
        
        int loop2 = 50;
        while(loop2 <= 70){
            System.out.print(loop2 + " ");
            loop2++;
        }
        
        System.out.println("\n");
         
        int loop3 = 0;
        while(loop3 <= 100){
            System.out.print(loop3 + " ");
            loop3 += 5;
        }
        
        System.out.println("\n");
        
        int loop4 = 20;
        while(loop4 > 0){
            System.out.print(loop4 + " ");
            loop4--;
        }
        
        System.out.println("\n");
        
        int loop5 = 0;
        while(loop5 < 15){
            System.out.println("computer");
            loop5++;
            
        }
        
        
    }
}